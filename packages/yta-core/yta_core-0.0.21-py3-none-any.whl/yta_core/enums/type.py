class _Type(Enum):
    """
    Enum class to represent the different types
    that exist in the app in a general context.
    
    For internal use only. Use a more specific
    Enum class to interact with the different
    types.
    """

    CUSTOM_STOCK = 'custom_stock'
    """
    Stock content but for our custom sources.
    """
    STOCK = 'stock'
    """
    Stock content from oficial sources.
    """
    AI_IMAGE = 'ai_image'
    """
    Content generated with AI generated images.
    """
    IMAGE = 'image'
    """
    Content generated with real images.
    """
    AI_VIDEO = 'ai_video'
    """
    Content generated with AI generated videos.
    """
    STICKER = 'sticker'
    """
    Content generated with images that are turned
    into stickers.
    """
    VIDEO = 'video'
    """
    Content generated with real videos.
    """
    SOUND = 'sound'
    """
    Content generated with real sounds.
    """
    YOUTUBE_VIDEO = 'youtube_video'
    """
    Content generated from videos obtained from
    the Youtube platform.
    """
    TEXT = 'text'
    """
    Content generated with text created with our
    own libraries.
    """
    MEME = 'meme'
    """
    Content generated with memes obtained from
    our own Youtube platform channels.
    """
    EFFECT = 'effect'
    """
    Content generated with effects made by 
    ourselves and applied to it.
    """
    PREMADE = 'premade'
    """
    Content generated by our own libraries.
    """
    GREENSCREEN = 'greenscreen'
    """
    Content generated with static and moving
    greenscreens to improve it.
    """

